#include <calc.h>

/**
 * op_add - function to make add operation
 *
 * @a: The first number
 *
 * @b: The second number
 *
 * Return: The result
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to make add operation
 *
 * @a: The first number
 *
 * @b: The second number
 *
 * Return: The result
*/

int op_sub(int a, int b)
{
        return (a - b);
}	

/**
 * op_mul - function to make add operation
 *
 * @a: The first number
 *
 * @b: The second number
 *
 * Return: The result
*/

int op_mul(int a, int b)
{
        return (a * b);
}

/**
 * op_div - function to make add operation
 *
 * @a: The first number
 *
 * @b: The second number
 *
 * Return: The result
*/

int op_div(int a, int b)
{
        return (a / b);
}

/**
 * op_mod - function to make add operation
 *
 * @a: The first number
 *
 * @b: The second number
 *
 * Return: The result
*/

int op_mod(int a, int b)
{
        return (a % b);
}
